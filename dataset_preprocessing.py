import os
import cv2
import numpy as np

IMAGE_DIR = "/content/drive/MyDrive/Infyntrek DS&ML Group 1 - Task 3/Images"
MASK_DIR = "/content/drive/MyDrive/Infyntrek DS&ML Group 1 - Task 3/Masks"

IMG_SIZE = 256

image_files = sorted(os.listdir(IMAGE_DIR))
mask_files = sorted(os.listdir(MASK_DIR))

print("Total Images:", len(image_files))
print("Total Masks:", len(mask_files))

assert len(image_files) == len(mask_files), "Number of images and masks do not match!"

for img, mask in zip(image_files, mask_files):
    assert img == mask, f"Mismatch found: {img} != {mask}"

print("All image-mask pairs are matched.")

images = []
masks = []

skipped = []

for filename in image_files:

    image_path = os.path.join(IMAGE_DIR, filename)
    mask_path = os.path.join(MASK_DIR, filename)
    image = cv2.imread(image_path)
    mask = cv2.imread(mask_path, cv2.IMREAD_GRAYSCALE)
    if image is None or mask is None:
        skipped.append(filename)
        continue

    image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)


    image = cv2.resize(image, (IMG_SIZE, IMG_SIZE))


    image = image.astype(np.float32) / 255.0


    mask = cv2.resize(
        mask,
        (IMG_SIZE, IMG_SIZE),
        interpolation=cv2.INTER_NEAREST
    )


    mask = (mask > 127).astype(np.uint8)


    mask = np.expand_dims(mask, axis=-1)

    images.append(image)
    masks.append(mask)

print("Preprocessing Completed.")
print("Skipped Files:", skipped)

X = np.array(images, dtype=np.float32)
Y = np.array(masks, dtype=np.uint8)

print("Image Shape :", X.shape)
print("Mask Shape  :", Y.shape)

print("Image Pixel Range:")
print("Minimum:", X.min())
print("Maximum:", X.max())

print("\nMask Values:")
print(np.unique(Y))

SAVE_PATH = "/content/drive/MyDrive/Infyntrek DS&ML Group 1 - Task 3"

np.save(
    os.path.join(SAVE_PATH, "preprocessed_images.npy"),
    X
)

np.save(
    os.path.join(SAVE_PATH, "preprocessed_masks.npy"),
    Y
)

print("Preprocessed dataset saved successfully!")
print(f"Images saved at: {os.path.join(SAVE_PATH, 'preprocessed_images.npy')}")
print(f"Masks saved at : {os.path.join(SAVE_PATH, 'preprocessed_masks.npy')}")